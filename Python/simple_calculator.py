# This is a sample calculator program to multiply two numbers
# + is for addition, - for subtraction, * for multiplication, / for division
print("This is a simple calculator that prompts the user to input two numbers and multiply them")
first_number = input('Enter the first number: ') # prompts the user to enter the first number and assign it using input library
second_number = input('Enter the second number: ') # prompts the user to input a second number
print(int(first_number) * int(second_number)) # convert the numbers to integer type and perform multiplication of the two numbers and prints the result on the terminal
