# Celcius to Fahrenheit Temperature Converter

degreesCelcius = int(input("Enter the Temperature in Celcius: ")) #prompt the user to enter temperature in celcius
degreesFahrenheit = int(degreesCelcius * (9/5) + 32) #convert degree celcius to Fahrenheit

print(f"The Temperature is {degreesFahrenheit} degrees Fahrenheit") #print degrees Fahrenheit